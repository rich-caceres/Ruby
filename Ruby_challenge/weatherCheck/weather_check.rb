puts "what is the current weather in your area?\n"

weather ='rain'

case weather

when 'cold'

    puts "Put a coat on, its going to be cold out."

when 'hot'

    puts "Wear some shorts, its a hot one today."

when 'rain'

    puts "it's rainy out, dont forget your umbrella!"

when 'snow'

    puts "It's snowing outside, bring a shovel and some gloves."

when 'windy'

    puts "Hold on to your hat folks, it's a windy day out there."

when 'foggy'

    puts "Be careful on the roads, visibility is limited with this fog."

when 'thunderstorms'

    puts "Thunderstorms are near, seak shelter and stay away from tall object."

end